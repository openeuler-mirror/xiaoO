    #[test]
    fn test_parse_toml_rejects_removed_network_config_field() {
        let toml = r#"
[path_groups]
network_config = true
"#;

        let error = Policy::from_toml(toml).unwrap_err();
        let message = error.to_string();

        assert!(message.contains("unknown field"));
        assert!(message.contains("network_config"));
    }

    #[test]
    fn test_parse_toml_with_read_write_execute_permission() {
        let toml = r#"
[[custom_paths]]
path = "/workspace/bin"
permission = "readwriteexecute"
"#;

        let policy = Policy::from_toml(toml).unwrap();
        let rule = policy.custom_paths.first().unwrap();

        assert_eq!(rule.path, PathBuf::from("/workspace/bin"));
        assert_eq!(rule.permission, FsPermission::ReadWriteExecute);
    }

