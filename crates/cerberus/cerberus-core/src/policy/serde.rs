    #[test]
    fn test_positive_resource_limits_preserved() {
        let mut policy = Policy::strict();
        policy.resources.max_memory_bytes = Some(512 * 1024 * 1024);
        policy.resources.max_processes = Some(100);

        let toml = policy.to_toml().unwrap();
        let restored = Policy::from_toml(&toml).unwrap();

        assert_eq!(
            restored.resources.max_memory_bytes,
            Some(512 * 1024 * 1024),
            "Positive memory limit should be preserved"
        );
        assert_eq!(
            restored.resources.max_processes,
            Some(100),
            "Positive process limit should be preserved"
        );
    }
