    #[test]
    fn test_example_policies_parse_correctly() {
        let examples = [
            (
                "high-security-agent",
                include_str!("../../../../../config/cerberus-policies/high-security-agent.toml"),
            ),
            (
                "repo-root-write-network-off",
                include_str!(
                    "../../../../../config/cerberus-policies/repo-root-write-network-off.toml"
                ),
            ),
            (
                "repo-root-write-network-on",
                include_str!(
                    "../../../../../config/cerberus-policies/repo-root-write-network-on.toml"
                ),
            ),
            (
                "repo-root-write-network-on-dev-env",
                include_str!(
                    "../../../../../config/cerberus-policies/repo-root-write-network-on-dev-env.toml"
                ),
            ),
            (
                "repo-root-python-network-filtered",
                include_str!(
                    "../../../../../config/cerberus-policies/repo-root-python-network-filtered.toml"
                ),
            ),
            (
                "minimal-example",
                include_str!("../../../../../config/cerberus-policies/minimal-example.toml"),
            ),
            (
                "llm-safe-example",
                include_str!("../../../../../config/cerberus-policies/llm-safe-example.toml"),
            ),
            (
                "process-visible-example",
                include_str!(
                    "../../../../../config/cerberus-policies/process-visible-example.toml"
                ),
            ),
            (
                "strict-example",
                include_str!("../../../../../config/cerberus-policies/strict-example.toml"),
            ),
        ];

        for (name, toml_content) in examples {
            let policy = Policy::from_toml(toml_content)
                .unwrap_or_else(|e| panic!("Failed to parse {}: {}", name, e));

            let roundtrip = Policy::from_toml(&policy.to_toml().unwrap())
                .unwrap_or_else(|e| panic!("Failed to roundtrip {}: {}", name, e));

            assert_eq!(
                policy.landlock_optional, roundtrip.landlock_optional,
                "landlock_optional mismatch for {}",
                name
            );
            assert_eq!(
                policy.mount_isolation_fallback, roundtrip.mount_isolation_fallback,
                "mount_isolation_fallback mismatch for {}",
                name
            );
            assert_eq!(
                policy.custom_paths.len(),
                roundtrip.custom_paths.len(),
                "custom_paths length mismatch for {}",
                name
            );
            assert_eq!(
                policy
                    .custom_paths
                    .iter()
                    .map(|rule| (&rule.path, &rule.permission))
                    .collect::<Vec<_>>(),
                roundtrip
                    .custom_paths
                    .iter()
                    .map(|rule| (&rule.path, &rule.permission))
                    .collect::<Vec<_>>(),
                "custom_paths mismatch for {}",
                name
            );
        }
    }

