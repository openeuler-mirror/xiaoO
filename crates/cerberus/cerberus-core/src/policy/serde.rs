//! Policy serialization and deserialization.

use super::{FsPermission, Policy};
use crate::policy::PolicyError;
use std::fs;
use std::io::Write;
use std::path::PathBuf;

impl Policy {
    /// Load policy from a TOML file.
    pub fn from_file(path: &PathBuf) -> Result<Self, PolicyError> {
        let contents = fs::read_to_string(path)
            .map_err(|e| PolicyError::FileError(path.clone(), e.to_string()))?;
        Self::from_toml(&contents)
    }

    /// Parse policy from TOML string.
    pub fn from_toml(toml: &str) -> Result<Self, PolicyError> {
        let policy: Policy =
            toml::from_str(toml).map_err(|e| PolicyError::ParseError(e.to_string()))?;

        policy.validate()?;

        Ok(policy)
    }

    /// Serialize policy to human-readable TOML.
    pub fn to_toml(&self) -> Result<String, PolicyError> {
        let mut output = String::new();

        output.push_str("# Cerberus Policy Configuration\n");
        output.push_str("# Generated by: cerberus\n");
        output.push_str("#\n");
        output.push_str("# This file configures the sandbox security policy.\n");
        output.push_str("# Edit values below to customize isolation behavior.\n");
        output.push('\n');

        output.push_str("# Network Access (matches namespaces.network directly)\n");
        output.push_str("# allow_network = true  -> namespaces.network = true\n");
        output.push_str("# allow_network = false -> namespaces.network = false\n");
        output.push_str(&format!(
            "# Current: allow_network = {}\n\n",
            self.allow_network()
        ));

        output.push_str("# Isolation Fallback Options\n");
        output.push_str(
            "# These flags only relax Landlock and mount-isolation downgrade behavior.\n",
        );
        output.push_str(
            "# They do not bypass namespace requirements or enabled network_policy gating.\n",
        );
        output
            .push_str("# landlock_optional = true allows execution when Landlock is unavailable\n");
        output.push_str(&format!(
            "landlock_optional = {}    # true = continue without Landlock, false = fail if unavailable\n",
            self.landlock_optional
        ));
        output.push_str(
            "# mount_isolation_fallback = true allows fallback to mount-only isolation\n",
        );
        output.push_str(&format!(
            "mount_isolation_fallback = {}    # true = use mount fallback, false = no fallback\n\n",
            self.mount_isolation_fallback
        ));

        output.push_str("# Custom Filesystem Rules\n");
        output.push_str("# Add paths beyond the predefined groups above.\n");
        output.push_str("#\n");
        output.push_str("# [[custom_paths]]\n");
        output.push_str("# path = \"/your/custom/path\"\n");
        output.push_str(
            "# permission = \"readonly\" | \"readwrite\" | \"readexecute\" | \"readwriteexecute\"\n",
        );
        output.push_str("#\n");
        output.push_str("# Permissions:\n");
        output.push_str("#   readonly     = can read files, cannot write or execute\n");
        output.push_str(
            "#   readwrite    = can read, write, create, replace, and delete files, cannot execute\n",
        );
        output.push_str("#   readexecute  = can read and execute files, cannot write\n");
        output.push_str(
            "#   readwriteexecute = can read, write, create, replace, delete, and execute files\n",
        );
        if self.custom_paths.is_empty() {
            output.push_str("custom_paths = []\n\n");
        } else {
            for rule in &self.custom_paths {
                let perm = match &rule.permission {
                    FsPermission::ReadOnly => "readonly",
                    FsPermission::ReadWrite => "readwrite",
                    FsPermission::ReadExecute => "readexecute",
                    FsPermission::ReadWriteExecute => "readwriteexecute",
                };
                output.push_str("[[custom_paths]]\n");
                output.push_str(&format!("path = \"{}\"\n", rule.path.display()));
                output.push_str(&format!("permission = \"{}\"\n\n", perm));
            }
        }

        output.push_str("# Path Groups - Toggle Predefined Filesystem Access\n");
        output.push_str("# true  = ALLOW access to these paths\n");
        output.push_str("# false = BLOCK access to these paths\n");
        output.push_str("#\n");
        output.push_str("[path_groups]\n");
        output.push_str("# /usr/bin - System executables (ls, cat, python, etc.)\n");
        output.push_str(&format!(
            "system_binaries = {}    # true = read+execute, false = blocked\n",
            self.path_groups.system_binaries
        ));
        output.push_str("# /usr/lib, /lib, /lib64 - Shared libraries (.so files)\n");
        output.push_str(&format!(
            "system_libraries = {}   # true = read+execute, false = blocked\n",
            self.path_groups.system_libraries
        ));
        output.push_str("# /tmp - Temporary files directory\n");
        output.push_str(&format!(
            "temp_directories = {}   # true = read+write, false = blocked\n",
            self.path_groups.temp_directories
        ));
        output.push_str("# /dev - Device files (null, zero, random, tty, etc.)\n");
        output.push_str(&format!(
            "device_files = {}       # true = read+write, false = blocked\n",
            self.path_groups.device_files
        ));
        output.push_str("# /proc - Process information filesystem\n");
        output.push_str(&format!(
            "proc_filesystem = {}    # true = read-only, false = blocked\n",
            self.path_groups.proc_filesystem
        ));
        output.push_str("# /mnt/wsl - WSL-specific files (resolv.conf symlink target)\n");
        output.push_str(&format!(
            "wsl_paths = {}          # true = read-only, false = blocked\n\n",
            self.path_groups.wsl_paths
        ));

        output.push_str("# Namespace Configuration\n");
        output.push_str("# mount, pid, and user keep their direct isolation meaning.\n");
        output.push_str("# network uses positive public semantics: true = allow network, false = block network.\n");
        output.push_str("#\n");
        output.push_str("[namespaces]\n");
        output.push_str("# mount = Filesystem mount points isolated from host\n");
        output.push_str(&format!(
            "mount = {}    # true = isolated, false = shared with host\n",
            self.namespaces.mount
        ));
        output.push_str("# pid = Process IDs isolated (cannot see host processes)\n");
        output.push_str(&format!(
            "pid = {}      # true = isolated, false = can see host processes\n",
            self.namespaces.pid
        ));
        output.push_str("# network = Whether network access is allowed\n");
        output.push_str("# true = network allowed, false = network blocked\n");
        output.push_str(&format!(
            "network = {}  # true = network allowed, false = network blocked\n",
            self.namespaces.network
        ));
        output.push_str("# user = User/group IDs isolated (unprivileged operation)\n");
        output.push_str("# NOTE: user is independent from network semantics. Choose the combination your runtime supports.\n");
        output.push_str(&format!(
            "user = {}     # true = isolated (unprivileged), false = real UID/GID\n\n",
            self.namespaces.user
        ));

        output.push_str("# Resource Limits\n");
        output.push_str("[resources]\n");
        output.push_str("# Maximum execution time in seconds (process killed if exceeded)\n");
        output.push_str(&format!("timeout_secs = {}\n", self.resources.timeout_secs));
        output.push_str("# Maximum memory in bytes (process fails allocation if exceeded)\n");
        output.push_str(
            "# Omit this field or set to a positive value for a limit (unlimited if omitted)\n",
        );
        match self.resources.max_memory_bytes {
            Some(mem) if mem > 0 => output.push_str(&format!(
                "max_memory_bytes = {}    # {} MB\n",
                mem,
                mem / 1024 / 1024
            )),
            _ => {} // Omit field for unlimited (None or normalized 0)
        }
        output.push_str("# Maximum number of processes (prevents fork bombs)\n");
        output.push_str(
            "# Omit this field or set to a positive value for a limit (unlimited if omitted)\n",
        );
        match self.resources.max_processes {
            Some(procs) if procs > 0 => output.push_str(&format!("max_processes = {}\n", procs)),
            _ => {} // Omit field for unlimited (None or normalized 0)
        }
        output.push('\n');

        output.push_str("# Environment Variables\n");
        output.push_str("# Only listed variables are passed to the sandboxed process.\n");
        output.push_str("# Common values: PATH, LANG, HOME, USER, TERM\n");
        output.push_str("[environment]\n");
        output.push_str(&format!("whitelist = {:?}\n", self.environment.whitelist));

        Ok(output)
    }

    /// Write policy to a TOML file.
    pub fn to_file(&self, path: &PathBuf) -> Result<(), PolicyError> {
        let toml = self.to_toml()?;
        let mut file = fs::File::create(path)
            .map_err(|e| PolicyError::FileError(path.clone(), e.to_string()))?;
        file.write_all(toml.as_bytes())
            .map_err(|e| PolicyError::FileError(path.clone(), e.to_string()))?;
        Ok(())
    }
}

