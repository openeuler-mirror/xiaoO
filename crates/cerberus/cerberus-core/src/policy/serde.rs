    #[test]
    fn test_example_policies_with_unlimited_resources_roundtrip_safely() {
        let examples = [
            (
                "minimal-example",
                include_str!("../../../../../config/cerberus-policies/minimal-example.toml"),
            ),
            (
                "process-visible-example",
                include_str!(
                    "../../../../../config/cerberus-policies/process-visible-example.toml"
                ),
            ),
        ];

        for (name, toml_content) in examples {
            let policy = Policy::from_toml(toml_content)
                .unwrap_or_else(|e| panic!("Failed to parse {}: {}", name, e));
            assert_eq!(
                policy.resources.max_processes, None,
                "{} should keep max_processes unlimited when omitted",
                name
            );

            let roundtrip_toml = policy.to_toml().unwrap();
            assert!(
                !roundtrip_toml.contains("max_processes = 0"),
                "{} should not serialize unlimited max_processes as 0",
                name
            );

            let roundtrip = Policy::from_toml(&roundtrip_toml)
                .unwrap_or_else(|e| panic!("Failed to roundtrip {}: {}", name, e));
            assert_eq!(
                roundtrip.resources.max_processes, None,
                "{} should keep unlimited max_processes after roundtrip",
                name
            );
        }
    }

    #[test]
    fn test_unlimited_resources_roundtrip_safely() {
        let mut policy = Policy::strict();
        policy.resources.max_memory_bytes = None;
        policy.resources.max_processes = None;

        let toml = policy.to_toml().unwrap();
        assert!(
            !toml.contains("max_memory_bytes = 0"),
            "TOML should not contain 'max_memory_bytes = 0' for unlimited"
        );
        assert!(
            !toml.contains("max_processes = 0"),
            "TOML should not contain 'max_processes = 0' for unlimited"
        );

        let restored = Policy::from_toml(&toml).unwrap();
        assert_eq!(
            restored.resources.max_memory_bytes, None,
            "Unlimited memory should round-trip as None"
        );
        assert_eq!(
            restored.resources.max_processes, None,
            "Unlimited processes should round-trip as None"
        );
    }

